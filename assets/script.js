const gamesListContainer = document.querySelector("#games-list");
const websListContainer = document.querySelector("#webs-list");
let rowSection = document.createElement("section");

const gamesList = document.createElement("section");
gamesList.className = "games-list";
const websList = document.createElement("section");
websList.className = "webs-list";

displayGames(myWork.myGamesList);
displayWebs(myWork.myWebPages);

function displayGames(games) {
  gamesList.innerText = "";
  games.map((game, index) => {
    if (index % 2 == 0) {
      rowSection = document.createElement("section");
      gamesList.append(rowSection);
    }
    const gameBox = document.createElement("section");
    gameBox.classList.add("list-box");

    // const img = document.createElement("img");
    // img.setAttribute("url", "./images/bg.jpg");
    // img.setAttribute("alt", game.alt);

    const name = document.createElement("section");
    name.innerText = game.name;
    name.classList.add("name");

    const netlify = document.createElement("a");
    netlify.style.fontWeight = "bold";
    netlify.href = game.netlifyLink;
    netlify.innerText = "Go Live";

    const newLine = document.createElement("br");
    const github = document.createElement("a");
    github.style.fontWeight = "bold";
    github.href = game.githubLink;
    github.innerText = "Code Link";

    const desc = document.createElement("section");
    desc.innerText = game.description;
    desc.classList.add("description");

    gameBox.append(name, netlify, newLine, github, desc);

    rowSection.appendChild(gameBox);
    gamesList.appendChild(rowSection);
    gamesListContainer.appendChild(gamesList);
  });
}

