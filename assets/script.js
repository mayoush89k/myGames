const searchBtn = document.querySelector("#search");

searchBtn.addEventListener("input", (event) => {
  const text = event.target.value;
  console.log(text);

  displayGames([
    ...myWork.myGamesList.filter((game) =>
      game.name.toLowerCase().includes(text.toLowerCase())
    ),
  ]);
  displayWebs([
    ...myWork.myWebPages.filter((web) =>
      web.name.toLowerCase().includes(text.toLowerCase())
    ),
  ]);
});
